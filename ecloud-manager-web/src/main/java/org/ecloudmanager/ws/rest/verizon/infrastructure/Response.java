package org.ecloudmanager.ws.rest.verizon.infrastructure;

import org.ecloudmanager.ws.rest.verizon.firewallrules.Reference;

/**
 * Rest API Response object
 */
public class Response {

    private String operation;
    private String status;
    private String errorMessage;
    private Reference initiatedBy;
    private String startTime;
    private String completedTime;

    public String getOperation() {
        return operation;
    }

    public void setOperation(String operation) {
        this.operation = operation;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getErrorMessage() {
        return errorMessage;
    }

    public void setErrorMessage(String errorMessage) {
        this.errorMessage = errorMessage;
    }

    public Reference getInitiatedBy() {
        return initiatedBy;
    }

    public void setInitiatedBy(Reference initiatedBy) {
        this.initiatedBy = initiatedBy;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getCompletedTime() {
        return completedTime;
    }

    public void setCompletedTime(String completedTime) {
        this.completedTime = completedTime;
    }

}
